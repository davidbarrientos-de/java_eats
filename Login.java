    private void jButtonLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonLoginActionPerformed
        String Correo = txt_correo.getText();
        String Contraseña = txt_contraseña.getText();
        if (Correo.equals("admin@javaeats.com") && Contraseña.equals("admin")){
            Administrador admin = new Administrador();
            admin.setVisible(true);
            admin.setLocationRelativeTo(null);
        } else if (UtilCliente.obtenerClaveporCorreo( Correo).equals(Contraseña)){
            if (UtilCliente.obtenerTCporCorreo(Correo).equals("Particular")){
                HomeParticular hm = new HomeParticular();
                hm.setVisible(true);
                hm.setLocationRelativeTo(null);
            } else {
                HomeEmpresa he = new HomeEmpresa();
                he.setVisible(true);
                he.setLocationRelativeTo(null);
            }
        } else {
            JOptionPane.showMessageDialog(this, "No existe el usuario o la contraseña introducida es incorrecta.", "Error", JOptionPane.ERROR_MESSAGE);
        }
        
        if (Correo.equals("") || Contraseña.equals("")){
            JOptionPane.showMessageDialog(this, "Tienen que rellenarse todos los campos", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButtonLoginActionPerformed

